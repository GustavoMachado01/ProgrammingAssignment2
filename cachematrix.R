makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setmatrix <- function(inv()) m <<- inv
  getmatrix <- function() m
  list(set = set, get = get,
       setmatrix = setmatrix,
       getmean = getmatrix())
}


cacheSolve <- function(x, ...) {
  m <- x$getmatrix()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- inv(data, ...)
  x$setmatrix(m)
  m
}
